def build_framework_user(instruction: str) -> str:
    return f"""
根据下面的 OpenClaw/用户指令，搭建一篇小红书笔记框架，并产出第一版正文草稿。

必须使用这个结构：

1. 精炼式开头，表达观点
- 用 1-2 句话清楚表达核心观点。
- 一篇笔记只说清楚一件事。
- 可以直接给出适合放进标题的观点句。
- 作用是快速吸引用户注意力，增加阅读、点赞、收藏的欲望。

2. 重要性 + 举例说明
- 围绕观点说明为什么重要，尽量连接用户痛点或长期影响。
- 举一个浅显、有共鸣的例子，可以是用户经历、生活场景、他人故事或可泛化案例。
- 这一段要浓缩，不要自嗨式铺陈，不要写成很长的故事。
- 作用是挖掘痛点，引起情感共鸣。

3. 落地方案/建议
- 给出实际行动指令，用 1/2/3/4 点输出。
- 每一点都要具体、可执行，不要只写抽象原则。
- 作用是提供利他价值，让用户觉得值得收藏。

4. 金句结尾
- 用一句有记忆点的话收尾，可以是价值主张、原创金句或简短引用。
- 再次肯定核心观点。
- 作用是提升笔记格调，引发共鸣，拉升互动。

输出要求：
- 先输出 `内容框架`，再输出 `正文草稿`。
- `内容框架` 里明确：目标人群、核心观点、用户痛点、共鸣例子、行动建议、金句方向。
- `正文草稿` 必须按上面的四段结构写，但不要显得像模板。
- 语气自然，有真实分享感，适合小红书。
- 不要使用空泛套话、万能句、过度排比。
- 不要新增无法从指令推出的具体事实。

指令：
{instruction}
""".strip()


def generate_titles_user(draft: str) -> str:
    return f"""
基于下面的内容框架和正文草稿，生成 8 个小红书标题。

标题方法：

1. 找到爆文关键词
- 先从正文里提炼 3-5 个“爆文关键词”。
- 爆文关键词就是选题方向，也是用户真正感兴趣的话题。
- 不要只站在作者视角想当然，要优先选择用户会主动搜索、会收藏、会产生共鸣的词。
- 由于你不能实时搜索小红书发现页，请基于内容判断哪些关键词更像最近 7 天或半年内容里容易出现千赞笔记的方向；如果不确定，要选择更具体、更有痛点的关键词。

2. 爆文关键词扩写，但不要 1:1 搬运
- 标题可以借用成熟爆文选题的结构，但必须写出这篇内容自己的角度。
- 不要照搬常见标题，不要堆砌热词，不要为了流量偏离正文。
- 每个标题都要围绕同一个核心内容方向，但切入点要有变化。

3. 标题结构至少覆盖以下 5 类
- 转折冲突：先给反常识/反直觉，再点出观点。
- 对比冲击：用前后、好坏、常见误区与正确做法形成对比。
- 痛点 + 解决办法：先戳用户痛点，再给解决方向。
- 假设 + 干货：用“如果你...”引出具体价值。
- 动作 + 数字 + 结果：用具体动作、数字和可感知结果增强收藏感。

要求：
1. 先输出 `爆文关键词`，再输出 `标题候选`。
2. 爆文关键词输出 3-5 个，并简单标注它对应的用户兴趣或痛点。
3. 每个标题 20 个中文字符以内，含标点符号统计在内。
4. 标题要优先承接正文的核心观点，而不是只蹭热点。
5. 不要夸大效果，不要使用虚假承诺。
6. 标题要像真人创作者写的，不要模板味太重。

内容：
{draft}
""".strip()


