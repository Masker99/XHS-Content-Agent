def humanize_copy_user(draft: str, titles: str, selected_title: str, keywords: str) -> str:
    return f"""
请对下面的小红书正文草稿做“去 AI 味”改写。

核心目标：
- 保留事实、核心观点和用户价值，不新增无法验证的信息。
- 让它读起来像一个真实创作者写的，而不是模型整理出来的。
- 保留小红书适合的换行节奏，但不要把每段都写成整齐模板。

去 AI 味规则：
1. 删除填充短语
- 删除“总的来说、值得注意的是、此外、与此同时、显而易见、不可否认”等没必要的铺垫。
- 不要出现“希望这对你有帮助、如果你想了解更多”等聊天机器人尾巴。

2. 打破公式感
- 底层仍然保留“观点-痛点/例子-方案-收尾”，但表面不要写成教案或提纲。
- 避免“这不仅仅是……更是……”这类否定式排比。
- 不要强行三段式、三连词、三连排比；两点或四点都可以。

3. 变化句子节奏
- 长短句混合。允许短句单独成段。
- 不要每段都用同一种句式开头。
- 大声读起来要顺，不要像报告。

4. 减少宣传腔和万能句
- 少用“至关重要、核心关键、深度赋能、打造、沉淀、闭环、价值感、长期主义”等空泛词。
- 不写“制胜秘诀、拉开差距、人生翻盘、必看”等夸张表达，除非原文明确要求。
- 把抽象话改成具体动作、具体场景、具体感受。

5. 信任读者
- 不要过度解释显而易见的道理。
- 不要每句话都下结论，给读者一点自己感受到的空间。
- 能用简单句就不要绕成复杂句。

6. 保留真人感
- 可以适度使用“我发现、我以前也会、后来才意识到”这类第一人称。
- 可以承认一点不确定或复杂感，比如“这事不难，难的是一直做”。
- 允许一点轻微的不完美，但不要口水化。

7. 收尾方式
- 原来的“金句结尾”要改成自然、有记忆点的收尾。
- 如果句子听起来像海报标语或鸡汤，要降一档，写得更像真人会说的话。

输出要求：
1. 只输出最终可发布版本。
2. 开头直接进入正文，不要解释你做了哪些修改。
3. 末尾附上：
推荐标题：使用选中标题里的最佳标题
话题标签：只保留可直接发布的 # 标签，不要带理由。

正文草稿：
{draft}

标题候选：
{titles}

选中标题：
{selected_title}

关键词/标签：
{keywords}
""".strip()


